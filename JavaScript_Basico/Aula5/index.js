/*Reras de variáveis
 1- Não podemos criar variáveis com palavras reservadas.
 ex: let if; 
 O if é uma palavra reservada para estrutura de condicional.
 
 2- Variáveis precisam ter nomes significativos. 
 ex: let n = 'João';
 Isso faz com que o código fique confuso.

 3- Variáveis não podem começar com número.
 ex: let 1num = 1; 

 4- Não podem conter espaços ou traços.
 ex: let num_1 = 1:

 5- Utilizamos o camelCase
 ex: let nomeCliente = 'Luiz';

 6- Case-sensitive
 ex: let nomeCliente = 'Luiz';
     let nomecliente = 'Luiz';
 Neste caso, temos 2 variáveis diferentes. 

 7- Não utilize VAR, utilize LET.
*/
