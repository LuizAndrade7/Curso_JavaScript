/*Reras de constantes
 1- Não podemos criar constantes com palavras reservadas.
 
 2- Constantes precisam ter nomes significativos. 

 3- Constantes não podem começar com número.
 ex: const 1num = 1; 

 4- Não podem conter espaços ou traços.
 ex: const num_1 = 1:

 5- Utilizamos o camelCase
 ex: const nomeCliente = 'Luiz';

 6- Case-sensitive
 ex: const nomeCliente = 'Luiz';
     const nomecliente = 'Luiz';
 Neste caso, temos 2 variáveis diferentes. 

 7- Não utilize VAR, utilize CONST.
 */

const nome = 'João';
console.log(nome);